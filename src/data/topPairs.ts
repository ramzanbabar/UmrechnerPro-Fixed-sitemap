// Get pair slug for URL
export function getPairSlug(fromUnit: string, toUnit: string): string {
  return `${fromUnit}-in-${toUnit}`;
}

// Parse pair slug
export function parsePairSlug(slug: string): { fromUnit: string; toUnit: string } | null {
  const parts = slug.split('-in-');
  if (parts.length !== 2) return null;
  return { fromUnit: parts[0], toUnit: parts[1] };
}

// Get top pairs for a category
export function getTopPairsForCategory(categorySlug: string, limit: number = 30): TopPair[] {
  return topPairs
    .filter(pair => pair.categorySlug === categorySlug)
    .slice(0, limit);
}

// Get similar pairs (in same category, different from/to)
export function getSimilarPairs(categorySlug: string, fromUnit: string, toUnit: string, limit: number = 8): TopPair[] {
  return topPairs
    .filter(pair => 
      pair.categorySlug === categorySlug && 
      !(pair.fromUnit === fromUnit && pair.toUnit === toUnit)
    )
    .slice(0, limit);
}

// Get top 20 for homepage
export function getTop20Pairs(): TopPair[] {
  return topPairs.filter(pair => pair.searchVolume === 'high').slice(0, 20);
}

